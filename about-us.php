 <h2> Our Mission</h2>
The Caregiving and Ageing Reimagined for Europe (CARE) initiative, which is part of the of the European Institute of Innovation and Technology (EIT) – Health Educational Campus,<br>
 is a first step on the path to a long-term solution to bridge the gap between the demand for elder caregiving and the supply of elder caregivers. CARE is building an education and<br> 
 training programme that will supplement current elder caregiving skills and create opportunities for the development of thousands of new elder caregivers. It will also provide the<br>
  guidance and tools for current and emerging institutions in education, healthcare and caregiving.

</div>
<div style="margin-left: 150px;">

<h2>The Challenge of Ageing in Europe:- </h2>
It is well-known that the longevity miracle and falling birth rates have the world ageing at an unprecedented rate, with Europe near the front of the pack. By 2060, 155 million Euro-<br>
peans – 30 percent of the total European population – will be aged 65 and older.These demographic shifts are breaking down traditional social structures and placing pressure on <br>
existing caregiving resources for Europe’s elderly population in the process.

</div>
<div style="margin-left: 150px;">

<h2>Who are We?:-</h2>
CARE CAMPUS is a consortium supported by the European Institute of Innovation and Technology - EIT Health and including leading academic institutions, private corporations, non-<br>
for-profit associations, startups and SMEs, and the public sector. The aim of the consortium is to develop a state-of-the-art training programme and technologies to increase aware-<br>
ness and knowledge about the available practices and tools to improve healthy and active ageing, and to attract actors who will become the innovators, the policy makers, and the w-<br>
orkforce of this new economic/cultural/societal change related to Ageing.
</div>
<div style="margin-left: 150px;">
<h2>Our mission:-</h2>
Ensure improved quality of care for seniors based on the self-assessment of need and in consultation with caregivers
Increase the numbers of new employed caregivers for the elderly<br> 
(including and especially amongst the young, unemployed and migrants)Improve the quality of elder care delivered by defining standard protocols and building a comprehensive, access-<br>
ible e-based learning curriculum, which will draw on best practices today in diseases of the elderly (such as Alzheimer’s) and general daily care, but also extend to the critical areas of skin,<br>
 oral and nutritional health, as well as applications for “end of life” value;Improve productivity and coping mechanisms for employed, informal family elder caregivers, and enhance<br> sustainability of health systems; and
Encourage economic growth and job creation across Europe in the health care industry.
    

